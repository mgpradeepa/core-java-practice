public class SwapMarkedText {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
//		System.out.println("Swapper");
		for (int i = 1; i < 5; i++) {
			for (int j = 0 ; j < i;j++){
				System.out.print("*");
			}
			
			System.out.println("\n");
		}
	}
		


}
